fun main() {
    print("Enter number1: ")
    val num1 = readln().toInt()

    print("Enter number2: ")
    val num2 = readln().toInt()

    println("sum = ${num1+num2}")
    println("sub = ${num1-num2}")
    println("mul = ${num1*num2}")
    println("div = ${num1/num2}")
    println("mod = ${num1%num2}")
}